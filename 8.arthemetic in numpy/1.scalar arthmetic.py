# SCALAR ARTHMETIC: its a linear algebra term, means a single value, we can apply operations with scalar, a single value to entire arrays..

import numpy as np
arr = np.array([1,2,3,4])

print(arr + 1)   # ADDITION
print("-----------------------")
print(arr - 1)   # SUBTRACTION
print("-----------------------")
print(arr * 3)   # MULTIPLICATION
print("-----------------------")
print(arr / 4)   # DIVISION
print("-----------------------")
print(arr % 2)   # MODULUS  
print("-----------------------")
print(arr ** 2)  # EXPONENTIATION
print("-----------------------")
print(arr // 2)  # FLOOR DIVISION
print("-----------------------")